    {
        $flight = Flight::with('airline', 'user', 'fo', 'aircraft', 'depapt', 'arrapt')->where('id', $id)->first();

        if (is_null($flight->callsign)) {
            $flight->callsign = $flight->getCallsign();
            $flight->save();
        }

        return view('admin.flights.detailed', ['flight' => $flight]);
